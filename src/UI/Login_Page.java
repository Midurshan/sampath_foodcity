    public String login(String username, String password, String userType) {
        // Validate input fields
        if (username.isEmpty() || password.isEmpty() || userType.isEmpty() || userType.equals("Select")) {
            JOptionPane.showMessageDialog(null, "Please fill all the fields and select a valid user type.");
            return "empty fields";
        }

        try {
            // Prepare SQL query
            String query = "SELECT * FROM sign_details WHERE email_address=? AND password=? AND LOWER(user_type)=LOWER(?)";
            Connection con = database.getConnection();
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, userType);

            // Execute query
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                // Retrieve the user type from the database
                String dbUserType = rs.getString("user_type");
                JOptionPane.showMessageDialog(null, "Login successful. You are logged in as " + dbUserType);

                // Redirect based on user type
                if (dbUserType.equalsIgnoreCase("admin")) {
                    Admin_Dashboard adminDashboard = new Admin_Dashboard();
                    adminDashboard.setVisible(true);
                    return "success";
                } else if (dbUserType.equalsIgnoreCase("employee")) {
                    Employee_Dashboard employee_dashboard = new Employee_Dashboard();
                    employee_dashboard.setVisible(true);
                    return "success";
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid user type.");
                    return "invalid user type";
                }
            } else {
                // Invalid credentials
                JOptionPane.showMessageDialog(null, "Email or password is incorrect.");
                return "invalid credentials";
            }
        } catch (SQLException ex) {
            // Handle SQL exceptions
            JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
            return "error";
        } catch (HeadlessException ex) {
            // Handle any other exceptions
            JOptionPane.showMessageDialog(null, "Unexpected error: " + ex.getMessage());
            return "error";
        }
    }
